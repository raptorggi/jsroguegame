class Game {
  constructor(w, h, canvas){
    this.canvas = document.getElementById(canvas);
    this.ctx = this.canvas.getContext("2d");
    this.width = w;
    this.height = h;
    this.started = 1;
    this.game_field = Array(30).fill(null).map(()=>Array(40).fill(null)); // size 40 x 30
  }

  stop() {
    this.started = 0;
  }

  draw() {
    this.clear_screen();
    for (let i = 0; i < 30; i++) {
      for (let j = 0; j < 40; j++) {
        if (this.game_field[i][j] != null) {
          this.game_field[i][j].draw(j, i, this.ctx);
        }
      }
    }
  }
  clear_screen() {
    this.ctx.fillStyle = "#000000";
    this.ctx.fillRect(0, 0, this.width, this.height);
  }

  is_started() {
    return this.started;
  }

  context() {
    return this.ctx;
  }

  generate_game_field(data) {
    for (let i = 0; i < 30; i++) {
      for (let j = 0; j < 40; j++) {
        if (data[i][j] == 1) {
          this.game_field[i][j] = new Wall;
        }
      }
    }
  }
}

class Object {
  constructor() {
    this.width = 20;
    this.height = 20;
  }

  draw(posX, posY, ctx) {
  }
}

class Wall extends Object {
  constructor() {
    super();
  }

  draw(posX, posY, ctx) {
    let x = posX * this.width
    let y = posY * this.height
    ctx.fillStyle = '#d9d9d9';
    ctx.fillRect(x, y, this.width, this.height);
  }
}

